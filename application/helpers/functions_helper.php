//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
function dump($variable)
{
    echo "<pre>\n";
    var_dump($variable) . "\n";
    echo "</pre>\n";
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
function dumpdie($variable)
{
    echo "<pre>\n";
    var_dump($variable) . "\n";
    echo "</pre>\n";
    die();
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
function _money($value)
{
    $temp = "$" . number_format($value,"2",",",".");
    return($temp);
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
function _date($date)
{
    $temp   = explode("-",$date);
    $temp   = $temp[2] . "/" . $temp[1] . "/" . $temp[0];
    return($temp);
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
function _datetime($dateTime)
{
    if($dateTime!=NULL)
    {
        $date   = explode(" ",$dateTime)[0];
        $time   = explode(" ",$dateTime)[1];

        $temp   = explode("-",$date);
        $date   = $temp[2] . "/" . $temp[1] . "/" . $temp[0];

        return($date . " " . $time);
    }
}
